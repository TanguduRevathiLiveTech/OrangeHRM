package com.JavaVariables;

public class VariableExample {

	public static void main(String[] args) {
		byte a=10;
		System.out.println("The value of byte variable is :- "+a);
		
		short b=20;
		System.out.println("The value of short variable is :- "+b);
		
		int c=30;
		System.out.println("The value of int variable is :- "+c);
		
		long d=40;
		System.out.println("The value of long variable is :- "+d);
		
		float e=35.123456f;
		System.out.println("The value of float variable is :- "+e);
		
		double f=24.223487578997676898768;
		System.out.println("The value of double variable is :- "+f);
		
		char g='a';
		System.out.println("The character in char variable is :- "+g);
		
		float asciivariable='A';
		System.out.println("The value of float ascii variable is :- "+asciivariable);
		
		int ascivariable='A';
		System.out.println("The value of int ascii variable is :- "+ascivariable);
		
		
		String h="20";
		System.out.println("the value of string variable is :- "+h);
		
		String stringVariable="A";
		System.out.println("The value of string in stringVariable is:-"+stringVariable);
		
		String groupofdwords="abcd";
		System.out.println("The value of string in groupofwords variable is :- "+groupofdwords);
		
		String junkVariables="@#$%%%&*";
		System.out.println("The value of sting in junkVariables is :- "+junkVariables);
		
		String var="true";
		System.out.println("The value of boolean in booleanVariable is :- "+var);
		
		boolean booleanVariable=true;
		System.out.println("The value of boolean in booleanVariable is :- "+booleanVariable);
		
		booleanVariable=false;
		System.out.println("The value of boolean in booleanVariable is :- "+booleanVariable);		
		
	
	}

	}


