package com.LoopsStatements;

public class ForLoopExample {

	public static void main(String[] args) {
		int i;
		
		for(i=0;i<=5;i++) {
			System.out.println("the value of i variables is "+i);
		
		}
		for(i=5;i<=10;i++) {
			System.out.println("the value of i variables in second for loop "+i);
			
		}
		for(i=2;i>=-6;i--) {
			System.out.println(i);
		}
		System.out.println("the value of i "+i);
		
	

}
}


