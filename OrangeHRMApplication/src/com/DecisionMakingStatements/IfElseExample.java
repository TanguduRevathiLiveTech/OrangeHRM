package com.DecisionMakingStatements;

public class IfElseExample {

	public static void main(String[] args) {
		int x=10;
		int y=20;
		System.out.println("The value of int in x variable is :- "+x);
		System.out.println("The value of int in y variable is :- "+y);
		if(x>y)
		{
			System.out.println("The value of x is greater than value of y");
		}
		else {
			System.out.println("The value of x variable is less than value of y");
		}
	}

}
